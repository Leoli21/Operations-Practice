
public class OperationsPractice 
{

	public static void main(String[] args) 
	{
		int number;
		number = 1;
		System.out.println("Number: " + number);
		number += 5;
		System.out.println("Add 5: " + number);
		number /= 2;
		System.out.println("Divide value by 2: " + number);
		number *= 4;
		System.out.println("Multiply value by 4: " + number);
		number -= 6;
		System.out.println("Subtract value by 6: " + number);
		number %= 2;
		System.out.println("Modularly divide by 2: " + number);
	}

}
